package students.karina_cmile.lesson_8.level_1_intern;

public class Trader {
    private String name;
    private String city;

    public Trader(String name, String city) {
        this.name = name;
        this.city = city;
    }
    public String getName() {
        return this.name;
    }
    public String getCity() {
        return this.city;
    }
}
