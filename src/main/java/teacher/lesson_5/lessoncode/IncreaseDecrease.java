package teacher.lesson_5.lessoncode;

public class IncreaseDecrease {
    public static void main(String[] args) {

        int i = 0;
        System.out.println("i value is: " + i);

        //1 способ
        i = i + 1;
        System.out.println("i value is: " + i);

        //2 способ
        i += 1;
        System.out.println("i value is: " + i);

        //3 способ
        i++;
        System.out.println("i value is: " + i);

        //1 способ
        i = i - 1;
        System.out.println("i value is: " + i);

        //2 способ
        i -= 1;
        System.out.println("i value is: " + i);

        //3 способ
        i--;
        i--;
        i--;
        System.out.println("i value is: " + i);

    }
}
